// TestAllDocFiles_Returns11 verifies the doc file list has all 11 entries.
func TestAllDocFiles_Returns11(t *testing.T) {
	files := AllDocFiles()
	assert.Len(t, files, 11)

	names := make(map[string]bool)
	for _, f := range files {
		names[f.Name] = true
		assert.NotEmpty(t, f.TemplateName, "file %s must have a template", f.Name)
	}

	// Verify expected files.
	expected := []string{
		"AGENTS.md", "CLAUDE.md", "SKILL.md", "CLI_REFERENCE.md",
		"STATUS_MACHINE.md", "WORKFLOWS.md", "CONTEXT_CHAIN.md",
		"BLOCKED_HANDLING.md", "SESSION_PROTOCOL.md", "PATTERNS.md",
		"TROUBLESHOOTING.md",
	}
	for _, name := range expected {
		assert.True(t, names[name], "missing doc file: %s", name)
	}
}

// mustCreateGenerator creates a generator using the embedded templates.
func mustCreateGenerator(t *testing.T, outDir string, data *TemplateData) *Generator {
	t.Helper()

	// Find the templates directory relative to this test file.
	// Templates are in internal/docs/templates/
	tmplDir := "templates"

	gen, err := NewGenerator(tmplDir, outDir, data, nil)
	require.NoError(t, err)
	return gen
}

